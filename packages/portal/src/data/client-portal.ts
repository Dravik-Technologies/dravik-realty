// ─── Helper builders ──────────────────────────────────────────
function doc(
  id: string, name: string,
  type: ClientTransaction["documents"][number]["type"],
  status: ClientTransaction["documents"][number]["status"],
  uploadedAt?: string, dueDate?: string, transactionId?: string,
): ClientTransaction["documents"][number] {
  return { id, name, type, status, ...(uploadedAt && { uploadedAt }), ...(dueDate && { dueDate }), ...(transactionId && { transactionId }) };
}

function msg(id: string, from: "agent" | "client", fromName: string, content: string, timestamp: string, displayTime: string, read: boolean): ClientMessage {
  return { id, from, fromName, content, timestamp, displayTime, read };
}

function activity(id: string, date: string, actor: string, actorRole: ActivityEntry["actorRole"], message: string, type: ActivityEntry["type"]): ActivityEntry {
  return { id, date, actor, actorRole, message, type };
}

